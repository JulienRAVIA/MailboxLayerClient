<?php

use PHPUnit\Framework\TestCase;

final class ValidatorTest extends TestCase
{
    /** @var Xylis\MailboxLayer\Validator $validator */
    protected $validator;

    public function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->client = new Xylis\MailboxLayer\Client('0298171f62e0037d75944220b3f3b4f9');
    }

    public function testIsValidEmailAddress()
    {
        $this->assertTrue($this->client->validate('julien.ravia@gmail.com')->isValid());
    }

    public function testIsInvalidEmailAddress()
    {
        $this->expectException(InvalidArgumentException::class);

        $this->client->validate('xylis@github')->isValid();
    }

    public function testIsRoleEmailAddress()
    {
        $this->assertTrue($this->client->validate('contact@gmail.com')->isRoleEmailAddress());
    }

    public function testIsNonRoleEmailAddress()
    {
        $this->assertFalse($this->client->validate('julien.ravia@gmail.com')->isRoleEmailAddress());
    }

    public function testIsDisposableEmailAddress()
    {
        $this->assertTrue($this->client->validate('2837484@mailinator.com')->isDisposable());
    }

    public function testIsNonDisposableEmailAddress()
    {
        $this->assertFalse($this->client->validate('jrgfawkes@gmail.com')->isDisposable());
    }

    public function testIsFreeDeliveredEmailAddress()
    {
        $this->assertTrue($this->client->validate('julien.ravia@gmail.com')->isFreeDeliveredEmailAddress());
    }

    public function testIsNonFreeDeliveredEmailAddress()
    {
        $this->assertFalse($this->client->validate('contact@julienravia.fr')->isFreeDeliveredEmailAddress());
    }
}
